    private void btnEditDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditDataActionPerformed
    //Mendapatkan baris yang dipilih pada jTable
   int selectedRow = jTable1.getSelectedRow();
        if(selectedRow != -1){
            //Mendapatkan value dari kolom pertama (kolom ID) pada baris yang dipilih
            int id = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
                //Perintah SQL untuk menghapus data berdasarkan ID
                String sql = "DELETE FROM tbmahasiswa WHERE = " + id;
      try{
         stm.executeUpdate(sql);
         JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
         tambahTabel(); //Pemanggilan method loadData() untuk menampilkan data yang baru
      } catch(Exception e){
         JOptionPane.showMessageDialog(null, "Data Gagal Dihapus" +e.getMessage());
      }
   } else {
      JOptionPane.showMessageDialog(null, "Pilih Baris Data Terlebih Dahulu");
   }
 
    }//GEN-LAST:event_btnEditDataActionPerformed

