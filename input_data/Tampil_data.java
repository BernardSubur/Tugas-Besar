    public void tambahTabel(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Nama");
        tb.addColumn("NIM");
        tb.addColumn("Semester");
        tb.addColumn("Prodi");
        tb.addColumn("Fakultas");
        tb.addColumn("No Telpon");
        tb.addColumn("Status Vaksin");
        tb.addColumn("Keterangan Beasiswa");
        tb.addColumn("No Rekening");
        tb.addColumn("Asal Kampus");
        tb.addColumn("Kampus yang Dituju");
        
        jTable1.setModel(tb);
        
        try{
        
        res = stm.executeQuery("Select * from tbmahasiswa");
        while (res.next()){
            tb.addRow(new Object[]{res.getString("Nama"),
                res.getString("NIM"),res.getString("Semester"),res.getString("Prodi"),
            res.getString("Fakultas"),res.getString("No Telepon"), res.getString("Status Vaksin"), res.getString("Keterangan Beasiswa"),
            res.getString("No Rekening"), res.getString("Asal Kampus"), res.getString("Kampus yang dituju")});
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan" +e.getMessage());
  }
}

